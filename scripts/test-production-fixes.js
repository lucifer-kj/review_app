async function testSecurityImprovements() {
  console.log('üîç Testing security improvements');
  
  try {
    // Check if securityAudit utility exists
    const securityAuditPath = path.join(__dirname, '..', 'src', 'utils', 'securityAudit.ts');
    if (!fs.existsSync(securityAuditPath)) {
      console.log('‚ùå securityAudit utility not found');
      return false;
    }
    
    const securityAuditContent = fs.readFileSync(securityAuditPath, 'utf8');
    
    // Check for required security features
    const requiredFeatures = [
      'SecurityAudit',
      'runSecurityAudit',
      'sanitizeInput',
      'isValidUUID',
      'isValidEmail',
      'getSecurityHeaders'
    ];
    
    let allFeaturesExist = true;
    
    for (const feature of requiredFeatures) {
      if (securityAuditContent.includes(feature)) {
        console.log(`‚úÖ Security feature exists: ${feature}`);
      } else {
        console.log(`‚ùå Security feature missing: ${feature}`);
        allFeaturesExist = false;
      }
    }
    
    // Check for potential password logging (should not exist)
    const authFiles = [
      'src/hooks/useAuth.ts',
      'src/hooks/useMagicLinkHandler.ts',
      'src/pages/Login.tsx'
    ];
    
    let noPasswordLogging = true;
    
    for (const authFile of authFiles) {
      const filePath = path.join(__dirname, '..', authFile);
      if (fs.existsSync(filePath)) {
        const content = fs.readFileSync(filePath, 'utf8');
        if (content.includes('console.log') && content.includes('password')) {
          console.log(`‚ùå Potential password logging found in ${authFile}`);
          noPasswordLogging = false;
        }
      }
    }
    
    if (noPasswordLogging) {
      console.log('‚úÖ No password logging detected');
    }
    
    return allFeaturesExist && noPasswordLogging;
  } catch (error) {
    console.error('‚ùå Security improvements test failed:', error.message);
    return false;
  }
}

async function runAllTests() {
  console.log('üöÄ Starting Production Fixes Test\n');
  
  const tests = [
    { name: 'Real-time Updates', test: testRealtimeUpdates },
    { name: 'Environment Configuration', test: testEnvironmentConfiguration },
    { name: 'Error Handling', test: testErrorHandling },
    { name: 'Performance Optimizations', test: testPerformanceOptimizations },
    { name: 'Security Improvements', test: testSecurityImprovements }
  ];
  
  const results = [];
  
  for (const { name, test } of tests) {
    console.log(`\n--- ${name} ---`);
    try {
      const result = await test();
      results.push({ name, passed: result });
    } catch (error) {
      console.error(`‚ùå ${name} test failed with error:`, error.message);
      results.push({ name, passed: false });
    }
  }
  
  console.log('\nüìä Test Results Summary:');
  console.log('========================');
  
  let passedTests = 0;
  let totalTests = results.length;
  
  results.forEach(({ name, passed }) => {
    const status = passed ? '‚úÖ PASS' : '‚ùå FAIL';
    console.log(`${status} ${name}`);
    if (passed) passedTests++;
  });
  
  console.log(`\nüéØ Overall Result: ${passedTests}/${totalTests} tests passed`);
  
  if (passedTests === totalTests) {
    console.log('üéâ All production fixes implemented successfully!');
    console.log('\nüìù Production Readiness Checklist:');
    console.log('==================================');
    console.log('‚úÖ Real-time updates implemented');
    console.log('‚úÖ Environment configuration fixed');
    console.log('‚úÖ Error handling enhanced');
    console.log('‚úÖ Performance optimizations added');
    console.log('‚úÖ Security improvements implemented');
    console.log('\nüöÄ Ready for production deployment!');
    return true;
  } else {
    console.log('‚ö†Ô∏è  Some production fixes need attention.');
    return false;
  }
}

// Run the tests
runAllTests()
  .then((success) => {
    process.exit(success ? 0 : 1);
  })
  .catch((error) => {
    console.error('‚ùå Test runner failed:', error);
    process.exit(1);
  });
